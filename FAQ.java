    private void b_submitActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_b_submitActionPerformed
        // TODO add your handling code here: try
    try {
        Connection con=new Connector().getCon();
        PreparedStatement ps=con.prepareStatement("INSERT INTO `asdproject`.`user_query` (`user_id`, `query`) VALUES (?, ?);");
        ps.setString(1,Integer.toString(id));
        ps.setString(2, ta_question.getText());
        int rs=ps.executeUpdate();
        if(rs>0)
        {
        JOptionPane.showMessageDialog(null,"Query Submitted Successfully");
        ta_question.setText("");
        }
        
    }
        catch(ClassNotFoundException e) {
            JOptionPane.showMessageDialog(null,"Class Error. Contact System Admin");
        } catch(SQLException e) {
            //e.printStackTrace();
            JOptionPane.showMessageDialog(null,"Database Error. Contact System Admin");
        } catch(Exception e) {
            JOptionPane.showMessageDialog(null,"Unknown Error. Contact System Admin");
        } 
        
    }//GEN-LAST:event_b_submitActionPerformed

